fun.flipud <-
function(data)
########################################################
#fun.flipud(matrix)
#Culculate flipud, return same results as Matlab
#######################################################
# version 0.1
# Jul 19, 2012
# Junlong Sun
# return [Output]
#######################################################
# Jul 19, 2012 - v0.1 Create
#######################################################
{
#-----------------------------------------------------------------#
## Initilization
#-----------------------------------------------------------------#
	
	if(class(data)!='matrix'){
		data<- matrix(data, nrow=length(data), ncol=1)
	}
	size <- dim(data)
	Output <- data

#-----------------------------------------------------------------#
## Calculate each column
#-----------------------------------------------------------------#
	for (j in 1:size[2]){
		for(i in 1:size[1]){
   			Output[i,j] <- data[size[1]+1-i,j]
		}
	}
#-----------------------------------------------------------------#

#-----------------------------------------------------------------#
## Return results
#-----------------------------------------------------------------#
	return(Output)
#-----------------------------------------------------------------#

}

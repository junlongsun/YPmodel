fun.cumsum <-
function(matrix)
########################################################
#fun.cumsum(matrix)
#Culculate cumsum, return same results as Matlab
#######################################################
# version 0.1
# May 6, 2012
# Junlong Sun
# return [Cumsum]
#######################################################
# May 6, 2012 - v0.1 Create
#######################################################
{
#-----------------------------------------------------------------#
## Initilization
#-----------------------------------------------------------------#
	size <- dim(matrix)
	Cumsum <- c()

#-----------------------------------------------------------------#
## Calculate each column
#-----------------------------------------------------------------#
	for(i in 1:size[2]){
   		Cumsum <- c(Cumsum,cumsum(matrix[1:size[1],i]))
	}
#-----------------------------------------------------------------#

#-----------------------------------------------------------------#
## Return results
#-----------------------------------------------------------------#
	return(Cumsum)
#-----------------------------------------------------------------#

}
